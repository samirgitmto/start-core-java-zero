package com.company;

public class CWH_22_do_while_loop {
    public static void main(String[] args) {
        int b = 10;       // main difference is that the 1st value enters into the loop unchecked and is executed.
        do {              // after 1st execution; it checks the condition.
            System.out.println(b);
            b++;
        }while (b<5);

        // Que -
    }
}
